package com.example.letmecookbe.enums;

public enum ReportSeverity {
    LOW,        // Thấp (ví dụ: nội dung không phù hợp nhẹ)
    MEDIUM,     // Trung bình (ví dụ: spam, thông tin sai lệch)
    HIGH,       // Cao (ví dụ: ngôn từ thù địch, quấy rối)
    CRITICAL    // Nghiêm trọng (ví dụ: lừa đảo, vi phạm bản quyền nghiêm trọng)
}